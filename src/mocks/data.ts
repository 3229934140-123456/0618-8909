function makePowerBanks(slotCount: number): PowerBank[] {
  const statuses: PowerBank["status"][] = [
    "available",
    "borrowed",
    "charging",
    "needs_recycle",
    "fault",
  ];
  const banks: PowerBank[] = [];
  for (let i = 0; i < slotCount; i++) {
    const rand = Math.random();
    let status: PowerBank["status"] = "available";
    if (rand < 0.35) status = "borrowed";
    else if (rand < 0.5) status = "charging";
    else if (rand < 0.55) status = "needs_recycle";
    else if (rand < 0.58) status = "fault";

    const batteryLevel =
      status === "borrowed"
        ? Math.floor(Math.random() * 40) + 10
        : status === "charging"
          ? Math.floor(Math.random() * 60) + 20
          : status === "needs_recycle"
            ? Math.floor(Math.random() * 15) + 5
            : status === "fault"
              ? 0
              : Math.floor(Math.random() * 40) + 60;

    banks.push({
      id: `pb-${Math.random().toString(36).slice(2, 8)}`,
      slotIndex: i + 1,
      batteryLevel,
      status,
      lastReportTime: new Date(
        Date.now() - Math.floor(Math.random() * 3600000)
      ).toISOString(),
    });
  }
  return banks;
}

